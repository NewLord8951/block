num = int(input("Введите номер дня недели: "))
if num == 1:
    print("Понедельник")
elif num == 2:
    print("Вторник")
elif num == 3:
    print("Среда")
elif num == 4:
    print("Четврег")
elif num == 5:
    print("Пятница")
elif num == 6:
    print("Суббота")
elif num == 7:
    print("Воскресенье")
else:
    print("Нет такого номера дня недели")
